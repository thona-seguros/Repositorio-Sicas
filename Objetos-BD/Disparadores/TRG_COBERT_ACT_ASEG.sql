                  TO_CHAR(:OLD.PRIMA_LOCAL)||' - '||TO_CHAR(:OLD.IDENDOSO)||' - '||TO_CHAR(:OLD.STSCOBERTURA)||' - '||TO_CHAR(:OLD.PLANCOB)||' - '||TO_CHAR(:OLD.COD_MONEDA)||' - '||TO_CHAR(:OLD.DEDUCIBLE_LOCAL)||' - '||
