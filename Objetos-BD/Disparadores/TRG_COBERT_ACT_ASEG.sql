      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'COBERT_ACT_ASEG', 'SE ELIMINA TODO EL REGISTRO', vl_Texto, 'REGISTRO ELIMINADO', 'DELETE',vl_Primary);
  END IF;
 
EXCEPTION
    WHEN OTHERS THEN 
