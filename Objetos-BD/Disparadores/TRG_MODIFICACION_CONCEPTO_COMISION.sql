    END IF;
    IF (NVL(:NEW.MTOCPTO,-20000) <> NVL(:OLD.MTOCPTO,-20000)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' NMODIFICACION: '||:OLD.NMODIFICACION||' CODCONCEPTO: '||:OLD.CODCONCEPTO||' CODTIPO: '||:OLD.CODTIPO;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'MODIFICACION_CONCEPTO_COMISION', 'MTOCPTO', TO_CHAR(:OLD.MTOCPTO), TO_CHAR(:NEW.MTOCPTO), 'UPDATE',vl_Primary);
    END IF;
