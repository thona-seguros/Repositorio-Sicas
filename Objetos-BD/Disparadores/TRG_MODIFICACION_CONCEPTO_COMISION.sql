      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' NMODIFICACION: '||:OLD.NMODIFICACION||' CODCONCEPTO: '||:OLD.CODCONCEPTO||' CODTIPO: '||:OLD.CODTIPO;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'MODIFICACION_CONCEPTO_COMISION', 'INDMODIF', TO_CHAR(:OLD.INDMODIF), TO_CHAR(:NEW.INDMODIF), 'UPDATE',vl_Primary);
    END IF;
    IF (NVL(:NEW.MTOMINAJUSTE,-20000) <> NVL(:OLD.MTOMINAJUSTE,-20000)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' NMODIFICACION: '||:OLD.NMODIFICACION||' CODCONCEPTO: '||:OLD.CODCONCEPTO||' CODTIPO: '||:OLD.CODTIPO;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'MODIFICACION_CONCEPTO_COMISION', 'MTOMINAJUSTE', TO_CHAR(:OLD.MTOMINAJUSTE), TO_CHAR(:NEW.MTOMINAJUSTE), 'UPDATE',vl_Primary);
    END IF;
    IF (NVL(:NEW.MTOMAXAJUSTE,-20000) <> NVL(:OLD.MTOMAXAJUSTE,-20000)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' NMODIFICACION: '||:OLD.NMODIFICACION||' CODCONCEPTO: '||:OLD.CODCONCEPTO||' CODTIPO: '||:OLD.CODTIPO;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'MODIFICACION_CONCEPTO_COMISION', 'MTOMAXAJUSTE', TO_CHAR(:OLD.MTOMAXAJUSTE), TO_CHAR(:NEW.MTOMAXAJUSTE), 'UPDATE',vl_Primary);
    END IF;
    IF (NVL(:NEW.MTOMAXIMO,-20000) <> NVL(:OLD.MTOMAXIMO,-20000)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' NMODIFICACION: '||:OLD.NMODIFICACION||' CODCONCEPTO: '||:OLD.CODCONCEPTO||' CODTIPO: '||:OLD.CODTIPO;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'MODIFICACION_CONCEPTO_COMISION', 'MTOMAXIMO', TO_CHAR(:OLD.MTOMAXIMO), TO_CHAR(:NEW.MTOMAXIMO), 'UPDATE',vl_Primary);
    END IF;
    IF (NVL(:NEW.MTOMINIMO,-20000) <> NVL(:OLD.MTOMINIMO,-20000)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' NMODIFICACION: '||:OLD.NMODIFICACION||' CODCONCEPTO: '||:OLD.CODCONCEPTO||' CODTIPO: '||:OLD.CODTIPO;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'MODIFICACION_CONCEPTO_COMISION', 'MTOMINIMO', TO_CHAR(:OLD.MTOMINIMO), TO_CHAR(:NEW.MTOMINIMO), 'UPDATE',vl_Primary);
    END IF;
    IF (NVL(:NEW.PRIORIDAD,-20000) <> NVL(:OLD.PRIORIDAD,-20000)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' NMODIFICACION: '||:OLD.NMODIFICACION||' CODCONCEPTO: '||:OLD.CODCONCEPTO||' CODTIPO: '||:OLD.CODTIPO;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'MODIFICACION_CONCEPTO_COMISION', 'PRIORIDAD', TO_CHAR(:OLD.PRIORIDAD), TO_CHAR(:NEW.PRIORIDAD), 'UPDATE',vl_Primary);
