      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' NMODIFICACION: '||:OLD.NMODIFICACION||' CODCONCEPTO: '||:OLD.CODCONCEPTO||' CODTIPO: '||:OLD.CODTIPO;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'MODIFICACION_CONCEPTO_COMISION', 'NMODIFICACION', TO_CHAR(:OLD.NMODIFICACION), TO_CHAR(:NEW.NMODIFICACION), 'UPDATE',vl_Primary);
