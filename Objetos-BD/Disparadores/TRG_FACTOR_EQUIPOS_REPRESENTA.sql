CREATE OR REPLACE TRIGGER SICAS_OC.TRG_FACTOR_EQUIPOS_REPRESENTA
  BEFORE INSERT OR UPDATE OR DELETE ON FACTOR_EQUIPOS_REPRESENTA
  FOR EACH ROW 
DECLARE
    vl_Texto    VARCHAR2(4000);
    vl_Primary  VARCHAR2(2000);
    vl_ErrorN   NUMBER;
    vl_ErrorC   VARCHAR2(4000);
BEGIN
  /*TRIGGER PARA MONITOREO DE LA TABLA FACTOR_EQUIPOS_REPRESENTA
    FECHA CREACIÓN: 12/06/2023
    MODIFICO: LUIS ARGENIS REYNOSO ALVAREZ
  */
  IF DELETING THEN 
      vl_Texto := TO_CHAR(:OLD.CODCIA)||' - '||TO_CHAR(:OLD.CODEMPRESA)||' - '||TO_CHAR(:OLD.IDTIPOSEG)||' - '||TO_CHAR(:OLD.PLANCOB)||' - '||TO_CHAR(:OLD.CODPAQUETE)||' - '||TO_CHAR(:OLD.CODEQUIPO)||' - '||
                  TO_CHAR(:OLD.INDINCLUIDO)||' - '||TO_CHAR(:OLD.FACTOR)||' - '||TO_CHAR(:OLD.TEXTOFACTOR)||' - '||TO_CHAR(:OLD.ACTUALIZO_USUARIO)||' - '||TO_CHAR(:OLD.ACTUALIZO_FECHA);
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'SE ELIMINA TODO EL REGISTRO', vl_Texto, 'REGISTRO ELIMINADO', 'DELETE',vl_Primary);
  END IF;
 
  IF INSERTING THEN 
      vl_Texto := TO_CHAR(:NEW.CODCIA)||' - '||TO_CHAR(:NEW.CODEMPRESA)||' - '||TO_CHAR(:NEW.IDTIPOSEG)||' - '||TO_CHAR(:NEW.PLANCOB)||' - '||TO_CHAR(:NEW.CODPAQUETE)||' - '||TO_CHAR(:NEW.CODEQUIPO)||' - '||
                  TO_CHAR(:NEW.INDINCLUIDO)||' - '||TO_CHAR(:NEW.FACTOR)||' - '||TO_CHAR(:NEW.TEXTOFACTOR)||' - '||TO_CHAR(:NEW.ACTUALIZO_USUARIO)||' - '||TO_CHAR(:NEW.ACTUALIZO_FECHA);
      vl_Primary := 'CODCIA: '||:NEW.CODCIA||' CODEMPRESA: '||:NEW.CODEMPRESA||' IDTIPOSEG: '||:NEW.IDTIPOSEG||' PLANCOB: '||:NEW.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:NEW.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'SE AGREGA NUEVO REGISTRO', vl_Texto, 'NUEVO REGISTRO', 'INSERT',vl_Primary);
  END IF;

  IF UPDATING THEN 
    IF (:NEW.CODCIA <> :OLD.CODCIA) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'CODCIA', TO_CHAR(:OLD.CODCIA), TO_CHAR(:NEW.CODCIA), 'UPDATE',vl_Primary);
    END IF;
    IF (:NEW.CODEMPRESA <> :OLD.CODEMPRESA) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'CODEMPRESA', TO_CHAR(:OLD.CODEMPRESA), TO_CHAR(:NEW.CODEMPRESA), 'UPDATE',vl_Primary);
    END IF;
    IF (:NEW.IDTIPOSEG <> :OLD.IDTIPOSEG) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'IDTIPOSEG', TO_CHAR(:OLD.IDTIPOSEG), TO_CHAR(:NEW.IDTIPOSEG), 'UPDATE',vl_Primary);
    END IF;
    IF (:NEW.PLANCOB <> :OLD.PLANCOB) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'PLANCOB', TO_CHAR(:OLD.PLANCOB), TO_CHAR(:NEW.PLANCOB), 'UPDATE',vl_Primary);
    END IF;
    IF (:NEW.CODPAQUETE <> :OLD.CODPAQUETE) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'CODPAQUETE', TO_CHAR(:OLD.CODPAQUETE), TO_CHAR(:NEW.CODPAQUETE), 'UPDATE',vl_Primary);
    END IF;
    IF (:NEW.CODEQUIPO <> :OLD.CODEQUIPO) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'CODEQUIPO', TO_CHAR(:OLD.CODEQUIPO), TO_CHAR(:NEW.CODEQUIPO), 'UPDATE',vl_Primary);
    END IF;
    IF (:NEW.INDINCLUIDO <> :OLD.INDINCLUIDO) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'INDINCLUIDO', TO_CHAR(:OLD.INDINCLUIDO), TO_CHAR(:NEW.INDINCLUIDO), 'UPDATE',vl_Primary);
    END IF;
    IF (:NEW.FACTOR <> :OLD.FACTOR) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'FACTOR', TO_CHAR(:OLD.FACTOR), TO_CHAR(:NEW.FACTOR), 'UPDATE',vl_Primary);
    END IF;
    IF (NVL(:NEW.TEXTOFACTOR,-20000) <> NVL(:OLD.TEXTOFACTOR,-20000)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'TEXTOFACTOR', TO_CHAR(:OLD.TEXTOFACTOR), TO_CHAR(:NEW.TEXTOFACTOR), 'UPDATE',vl_Primary);
    END IF;
    IF (NVL(:NEW.ACTUALIZO_USUARIO,-20000) <> NVL(:OLD.ACTUALIZO_USUARIO,-20000)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'ACTUALIZO_USUARIO', TO_CHAR(:OLD.ACTUALIZO_USUARIO), TO_CHAR(:NEW.ACTUALIZO_USUARIO), 'UPDATE',vl_Primary);
    END IF;
    IF (NVL(:NEW.ACTUALIZO_FECHA,SYSDATE) <> NVL(:OLD.ACTUALIZO_FECHA,SYSDATE)) THEN
      vl_Primary := 'CODCIA: '||:OLD.CODCIA||' CODEMPRESA: '||:OLD.CODEMPRESA||' IDTIPOSEG: '||:OLD.IDTIPOSEG||' PLANCOB: '||:OLD.PLANCOB;
      OC_LOG_CONF_FACT_ELECT.INSERTA(:OLD.CODCIA, 'FACTOR_EQUIPOS_REPRESENTA', 'ACTUALIZO_FECHA', TO_CHAR(:OLD.ACTUALIZO_FECHA), TO_CHAR(:NEW.ACTUALIZO_FECHA), 'UPDATE',vl_Primary);
    END IF;
  END IF;    

EXCEPTION
    WHEN OTHERS THEN 
      vl_ErrorN := SQLCODE;
      vl_ErrorC := 'Error con el disparador de FACTOR_EQUIPOS_REPRESENTA, Contacte al administrador. '||SQLERRM;
      raise_application_error( -20000,vl_ErrorC);
END;
/

CREATE OR REPLACE PUBLIC SYNONYM TRG_FACTOR_EQUIPOS_REPRESENTA FOR SICAS_OC.TRG_FACTOR_EQUIPOS_REPRESENTA;
