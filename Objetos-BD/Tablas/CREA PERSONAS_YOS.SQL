DROP PUBLIC SYNONYM PERSONAS_YOS
;
DROP TABLE PERSONAS_YOS
;

CREATE TABLE PERSONAS_YOS
(
TIPO_DOC_IDENTIFICACION    VARCHAR2(30),
NUM_DOC_IDENTIFICACION    VARCHAR2(30),
NUM_CONSECUTIVO_YO      NUMBER(14),
NOMBRE_COMERCIAL              VARCHAR2(100)   ,
DIRECCION_CALLE      VARCHAR2(100)  ,
NUM_EXTERIOR      VARCHAR2(5)  ,
NUM_INTERIOR      VARCHAR2(5)  ,
CODIGO_POSTAL          VARCHAR2(6)  ,
CODCOLONIA              VARCHAR2(6)  ,
CODESTADO      VARCHAR2(3)  ,
CODCIUDAD      VARCHAR2(3)  ,
CODMUNICIPIO      VARCHAR2(3)  ,
CODPAIS        VARCHAR2(3)  ,
TELEFONO      VARCHAR2(10) ,
EMAIL        VARCHAR2(50) ,

CONSTRAINT PERSONAS_YOS_PK PRIMARY KEY (TIPO_DOC_IDENTIFICACION, NUM_DOC_IDENTIFICACION, NUM_CONSECUTIVO_YO  )
)

TABLESPACE TS_SICASOC
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
;

