DROP PUBLIC SYNONYM PERSONAS_IDUNICOPERSONA
;
DROP TABLE PERSONAS_IDUNICOPERSONA
;

CREATE TABLE PERSONAS_IDUNICOPERSONA
(
IDUNICOPERSONA      	VARCHAR2(20),
TIPO_DOC_IDENTIFICACION     VARCHAR2(06),
NUM_DOC_IDENTIFICACION     VARCHAR2(20),
TIPO_ID_TRIBUTARIA	VARCHAR2(06),
NUM_TRIBUTARIO     	VARCHAR2(20),
CURP     			VARCHAR2(30)
)

TABLESPACE TS_SICASOC
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
;

-- =============================
-- Crea Comentarios
-- =============================
COMMENT ON TABLE PERSONAS_IDUNICOPERSONA IS 'Tabla  para PERSONAS_IDUNICOPERSONA'
/
COMMENT ON COLUMN PERSONAS_IDUNICOPERSONA.IDUNICOPERSONA   is 'Numero ID Unico Persona';
/
COMMENT ON COLUMN PERSONAS_IDUNICOPERSONA.TIPO_DOC_IDENTIFICACION IS 'Tipo de documento';
/
COMMENT ON COLUMN PERSONAS_IDUNICOPERSONA.NUM_DOC_IDENTIFICACION IS 'Numero de documento';
/
COMMENT ON COLUMN PERSONAS_IDUNICOPERSONA.TIPO_ID_TRIBUTARIA IS 'Tipo de documento Tributario';
/
COMMENT ON COLUMN PERSONAS_IDUNICOPERSONA.NUM_TRIBUTARIO    is 'Numero ID Tributrio';
/
COMMENT ON COLUMN PERSONAS_IDUNICOPERSONA.CURP    is 'Numero de CURP';

-- =============================
-- Crea indices
-- =============================
create  index PERSONAS_IDUNICOPERSONA_IDX_1 on PERSONAS_IDUNICOPERSONA (IDUNICOPERSONA);
/
create  index PERSONAS_IDUNICOPERSONA_IDX_2 on PERSONAS_IDUNICOPERSONA (TIPO_DOC_IDENTIFICACION,NUM_DOC_IDENTIFICACION);
/
create  index PERSONAS_IDUNICOPERSONA_IDX_3 on PERSONAS_IDUNICOPERSONA (TIPO_ID_TRIBUTARIA,NUM_TRIBUTARIO);
/
create  index PERSONAS_IDUNICOPERSONA_IDX_4 on PERSONAS_IDUNICOPERSONA (CURP);

