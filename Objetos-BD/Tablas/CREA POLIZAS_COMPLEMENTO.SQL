-- =============================
-- Crea Tabla
-- =============================
DROP PUBLIC SYNONYM POLIZAS_COMPLEMENTO
;
DROP TABLE POLIZAS_COMPLEMENTO
;

CREATE TABLE POLIZAS_COMPLEMENTO
(
CODCIA				NUMBER(14),
CODEMPRESA			NUMBER(14),
IDPOLIZA				NUMBER(14),
INDFACTPERSONALIZADA  		VARCHAR2(01),
FORMAIMPRIMENOMBRE  		VARCHAR2(01),

CONSTRAINT POLIZAS_COMPLEMENTO_PK PRIMARY KEY (CODCIA, CODEMPRESA, IDPOLIZA  )
)

TABLESPACE TS_SICASOC
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
;

-- =============================
-- Crea Comentarios
-- =============================
COMMENT ON TABLE POLIZAS_COMPLEMENTO IS 'Tabla  para POLIZAS_COMPLEMENTO'
/
COMMENT ON COLUMN POLIZAS_COMPLEMENTO.CODCIA IS 'Codigo Compañia';
/
COMMENT ON COLUMN POLIZAS_COMPLEMENTO.CODEMPRESA IS 'Codigo de Empresa';
/
COMMENT ON COLUMN POLIZAS_COMPLEMENTO.IDPOLIZA   is 'Numero Poliza';
/
COMMENT ON COLUMN POLIZAS_COMPLEMENTO.INDFACTPERSONALIZADA            is 'Indicador Facturacion personalizada';
/
COMMENT ON COLUMN POLIZAS_COMPLEMENTO.FORMAIMPRIMENOMBRE              is 'Forma imprime nombre';
/

-- =============================
-- Genera los permisos
-- =============================
GRANT SELECT, INSERT, UPDATE, DELETE, ALTER, INDEX ON POLIZAS_COMPLEMENTO to PUBLIC
;
-- =============================
-- Crea el Sinónimo
-- =============================
CREATE or REPLACE PUBLIC SYNONYM POLIZAS_COMPLEMENTO FOR POLIZAS_COMPLEMENTO
;
